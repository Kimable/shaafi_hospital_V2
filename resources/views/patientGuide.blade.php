          <h1 class="mb-4">Patient Guide: Hospitalization</h1>

          <h2>On the Day of Admission for Inpatient &amp; Day Surgery</h2>
          <p>On the day of your admission for inpatient or day surgery, it's important to follow these steps to ensure a
            smooth process:</p>
          <ol>
            <li><strong>Arrival:</strong> Arrive at the hospital at least one hour before your scheduled procedure.
              Check-in at the reception desk located in the main entrance of the hospital.</li>
            <li><strong>Registration:</strong> Present your identification documents and any relevant medical documents
              at the registration counter.</li>
            <li><strong>Medical History:</strong> A medical professional will review your medical history and any
              pre-operative instructions.</li>
            <li><strong>Consent Form:</strong> Sign the necessary consent forms after understanding the procedure and
              its risks.</li>
            <li><strong>Preparation:</strong> Follow any pre-surgery fasting or hygiene instructions provided by your
              healthcare provider.</li>
          </ol>

          <h2>Personal Valuables</h2>
          <p>While we strive to ensure a safe environment, it's advisable to leave your valuable items at home. However,
            if
            you do bring valuables, you can deposit them with the hospital staff for safekeeping during your stay. The
            hospital cannot be held responsible for any loss or damage to personal belongings.</p>

          <h2>Visiting Hours</h2>
          <p>Visitors are essential for your emotional well-being during your hospital stay. Our visiting hours are
            11am to 4pm. Please respect the comfort of other patients and limit the number of visitors at a time.
          </p>

          <h2>Things to Bring for Your Stay</h2>
          <p>To make your stay more comfortable, consider packing the following items:</p>
          <ul>
            <li><strong>Comfortable Clothing:</strong> Pajamas, robes, and non-slip slippers.</li>
            <li><strong>Toiletries:</strong> Toothbrush, toothpaste, shampoo, soap, and other personal care items.</li>
            <li><strong>Entertainment:</strong> Books, magazines, electronic devices, and headphones.</li>
            <li><strong>Medications:</strong> Any prescribed medications you're currently taking.</li>
            <li><strong>Identification:</strong> Identity card, insurance card, and any relevant medical documents.</li>
            <li><strong>Personal Items:</strong> Eyeglasses, hearing aids, and other essential personal items.</li>
          </ul>

          <h2>Patient Discharge</h2>
          <p>When you're ready for discharge, your healthcare provider will provide you with instructions for post-care.
            Here's what to expect:</p>
          <ol>
            <li><strong>Medication Instructions:</strong> Understand when and how to take your prescribed medications.
            </li>
            <li><strong>Follow-up Appointments:</strong> Schedule any necessary follow-up appointments with your doctor.
            </li>
            <li><strong>Home Care Instructions:</strong> Follow the provided guidelines for wound care, physical
              activity, and dietary restrictions.</li>
          </ol>

          <h2>Payment Guide</h2>
          <p>Hospital bills can be complex, but we're here to assist you. Here's a general overview:</p>
          <ol>
            <li><strong>Billing Process:</strong> After your treatment, you'll receive an itemized bill detailing the
              services you received.</li>
            <li><strong>Insurance:</strong> If you have insurance, contact your provider to understand your coverage.
              You may need to provide relevant information to the hospital for direct billing.</li>
            <li><strong>Payment Options:</strong> We accept various payment methods, including cash, credit/debit cards,
              and mobile payment apps.</li>
            <li><strong>Financial Assistance:</strong> If you require financial assistance, inquire about available
              options.</li>
          </ol>

          <p class="mt-4">Please note that this guide provides general information. For specific details related to
            your procedure, treatment, or condition, visit us or call us.</p>
